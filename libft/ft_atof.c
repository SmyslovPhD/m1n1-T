#include "libft.h"

double	ft_atof(const char *s)
{
	double	num;
	double	dot;
	int		sign;

	num = 0;
	dot = .1;
	sign = 1;
	while (*s == '\t' || *s == '\n' || *s == '\v'
			|| *s == '\f' || *s == '\r' || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
		sign -= *(s++) % '+';
	while (*s >= '0' && *s <= '9')
		num = num * 10 + (*(s++) - '0');
	if (*s++ == '.')
		while (*s >= '0' && *s <= '9')
		{
			num += dot * (*(s++) - '0');
			dot *= .1;
		}
	return (num * sign);
}
